/*
 * Сценарный контроллер действий: храню последовательность шагов и переключаю цели в зависимости от состояния.
 */

//   Сценарный контроллер хранит очередь действий и возвращает текущую цель для агента.
class Controller{
	//   Инициализирую сценарий действий и стартовый индекс шага.
	constructor(acts){
		this.acts = acts;
		this.cur = 0;
	}

	//   Возвращаю активный шаг сценария для текущего такта.
	get_current_task(){
		return this.acts[this.cur];
	}
}


module.exports = Controller;
